public class Task12 {
    public static void main(String[] args) {
        int a = 25,
            b = 39;
        System.out.println("25 != 39: "+ (a!=b));
        System.out.println("25 < 39: "+ (a<b));
        System.out.println("25 == 39: "+ (a==b));
        System.out.println("25 > 39: "+ (a>b));
        System.out.println("25 >= 39: "+ (a>=b));
    }
}
